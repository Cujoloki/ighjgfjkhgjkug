/**
 * Utility for managing seasonal themes in the farm application
 */

export type Season = 'spring' | 'summer' | 'fall' | 'winter';

/**
 * Determines the current season based on the date
 * @returns The current season
 */
export function getCurrentSeason(): Season {
  const now = new Date();
  const month = now.getMonth();
  
  // Northern hemisphere seasons
  if (month >= 2 && month <= 4) return 'spring';  // March to May
  if (month >= 5 && month <= 7) return 'summer';  // June to August
  if (month >= 8 && month <= 10) return 'fall';   // September to November
  return 'winter';                               // December to February
}

/**
 * Applies the seasonal theme to the document
 * @param season The season to apply, or auto-detect if not provided
 */
export function applySeasonalTheme(season?: Season): void {
  const currentSeason = season || getCurrentSeason();
  
  // Remove any existing seasonal classes
  document.documentElement.classList.remove('theme-spring', 'theme-summer', 'theme-fall', 'theme-winter');
  
  // Add the current season class
  document.documentElement.classList.add(`theme-${currentSeason}`);
  
  // You could also update a theme context here if using React context
}

