/**
 * Gets themed icon for the current season
 * @param season The season to get icon for
 * @returns Path to the seasonal icon
 */
export function getSeasonalIcon(iconName: string, season?: Season): string {
  const currentSeason = season || getCurrentSeason();
  
  // In a real implementation, you would have different icon sets for each season
  // For now, we'll just return the icon name with a season prefix
  return `/icons/${currentSeason}/${iconName}.svg`;
}

/**
 * Gets a friendly greeting based on the current season
 */
export function getSeasonalGreeting(): string {
  const season = getCurrentSeason();
  
  switch (season) {
    case 'spring':
      return "Spring planting time!";
    case 'summer':
      return "Summer growing season!";
    case 'fall':
      return "Fall harvest time!";
    case 'winter':
      return "Winter planning season!";
  }
}